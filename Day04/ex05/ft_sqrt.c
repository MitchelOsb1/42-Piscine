int	ft_sqrt(int nb)
{
	int i;

	if (nb == 1)
		return (1);
	i = nb / 2;
	while (i > 1)
	{
		if (nb % i == 0)
		{
			if (i * i == nb)
				return (i);
		}
		i--;
	}
	return (0);
}
